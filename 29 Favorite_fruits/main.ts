// Fefine variable
let favoriteFruit =["Mango", "Strawbery", "Guava"]

// Using 5 independent if statements
if (favoriteFruit.includes("Mango")){
    console.log("I really like Mango");
};
if (favoriteFruit.includes("Strawbery")){
    console.log("I really like strawbery");
};
if (favoriteFruit.includes("Apple")){
    console.log("I really like Apple")
};
if (favoriteFruit.includes("Guava")){
    console.log("I really like Guava")
};
if (favoriteFruit.includes("Bananas")){
    console.log("I really like Banans")
};

